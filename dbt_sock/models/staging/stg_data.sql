WITH raw_data as (
    SELECT *
    FROM  {{source('sock_data','sock_raw')}}
),
cleaned AS (
    SELECT

    LEAST(ABS(SOCK_AGE_DAYS)::INT, 730) AS SOCK_AGE_DAYS,
    COALESCE(LOWER(SOCK_TYPE), (
        SELECT LOWER(SOCK_TYPE)
        FROM raw_data
        GROUP BY SOCK_TYPE
        ORDER BY COUNT(*) DESC
        LIMIT 1
    )) AS SOCK_TYPE,

    CASE 
        WHEN COLOR_INTENSITY > 10 THEN 10
        ELSE ABS(COLOR_INTENSITY)
    END AS COLOR_INTENSITY,

    CASE 
        WHEN ABS(WASHER_SPIN_RPM) > 1400 THEN AVG(WASHER_SPIN_RPM) OVER()
        WHEN ABS(WASHER_SPIN_RPM) < 600 THEN AVG(WASHER_SPIN_RPM) OVER()
        ELSE ABS(WASHER_SPIN_RPM)
    END::INT AS WASHER_SPIN_RPM,

    ROUND(COALESCE(ABS(WASH_TEMP_C), AVG(WASH_TEMP_C) OVER() )::NUMERIC,2) AS WASH_TEMP_C,

    ROUND(COALESCE(ABS(DRYER_LINT_GRAMS), AVG(DRYER_LINT_GRAMS) OVER())::NUMERIC,2) AS DRYER_LINT_GRAMS,

    COALESCE(LOWER(LOAD_SIZE), (
        SELECT LOWER(LOAD_SIZE)
        FROM raw_data 
        GROUP BY LOAD_SIZE
        ORDER BY COUNT(*) DESC
        LIMIT 1
    )) AS LOAD_SIZE,

    COALESCE(LOWER(PHASE_OF_MOON), (
        SELECT LOWER(PHASE_OF_MOON)
        FROM raw_data 
        GROUP BY PHASE_OF_MOON
        ORDER BY COUNT(*) DESC
        LIMIT 1
    )) AS PHASE_OF_MOON,

    CASE
        WHEN DESPERATION_INDEX > 10 THEN 10
        ELSE ABS(DESPERATION_INDEX)
    END AS DESPERATION_INDEX,

    CASE 
        WHEN TARGET_VANISHED >= 1 THEN 1
        ELSE 0
    END AS TARGET_VANISHED

    FROM raw_data
)


SELECT 
    *
FROM cleaned
WHERE 
    SOCK_AGE_DAYS IS NOT NULL
    AND SOCK_TYPE IS NOT NULL
    AND COLOR_INTENSITY IS NOT NULL
    AND LOAD_SIZE IS NOT NULL
    AND PHASE_OF_MOON IS NOT NULL
    AND DESPERATION_INDEX IS NOT NULL
    AND TARGET_VANISHED IS NOT NULL